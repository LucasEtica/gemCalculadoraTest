module GemTesteCalculadora
    module Calculo
      def self.soma(a, b)
        return a + b
      end
  
      def self.menos(a, b)
        return a - b
      end
  
      def self.vezes(a, b)
        return a * b
      end
  
      def self.dividir(a, b)
        return a / b
      end
  
      def self.multiplicar(a, b)
        return a * b
      end
    end
  end